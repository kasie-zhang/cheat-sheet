#++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# Show date and time
#++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
#------------------------------
# Tue Sep 17 07:45:32 AM CST 2024
#------------------------------
date

#------------------------------
# 2024-09-17  07:46:40  Tuesday  September
#------------------------------
date  +"%Y-%m-%d  %H:%M:%S  %A  %B"

#------------------------------
# Month name: (September Sep)
#------------------------------
date +"%B %b"

#------------------------------
# Week number of the year
#------------------------------
date +"%U"

#------------------------------
# Day of the year
#------------------------------
date +"%j"

#------------------------------
# Day of the week: (2 Tuesday Tue)
#------------------------------
date +"%w  %A  %a"

#------------------------------
# Set timezone
#------------------------------
TZ="America/Newyork" date

#------------------------------
# Relative dates and times
#------------------------------
date -d "tomorrow"
date -d "yesterday"
# minitue, hour, day, week, month, year
date -d "2 hours"
date -d "2 hours ago"
# Get date by string
date -d "2024-12-23 11:12:13"
date -d "$(date) +2 minutes"

#------------------------------
# Get Unix timestamp
#------------------------------
date +%s

#------------------------------
# Convert timestamps
#------------------------------
date -d @1726530846

#------------------------------
# Set system date and time
#------------------------------
sudo date --set="2024-09-17"
sudo date --set="13:00:00"

#++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
# Useful scripts
#++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
#------------------------------
# Generate a range of date
#------------------------------
for d in {0..5}; do date -d "2024-09-10 +$d day" +"%Y-%m-%d"; done

#------------------------------
# Get the last day of the previous month
#------------------------------
date -d "$(date +%Y-%m-01) -1 day" +"%Y-%m-%d"
